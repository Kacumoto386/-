# ========== 预约时段（每10分钟一个） ==========
def _gen_10min_slots(start_hour=6, end_hour=22):
    """生成[start_hour:00, end_hour:00)范围内每10分钟的时段列表"""
    slots = []
    for h in range(start_hour, end_hour):
        for m in range(0, 60, 10):
            slots.append(f"{h:02d}:{m:02d}")
    return slots

